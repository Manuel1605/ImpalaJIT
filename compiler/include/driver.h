#ifndef IMPALAJIT_DRIVER_H
#define IMPALAJIT_DRIVER_H



#include <string>
#include <map>
#include <vector>

#include <function_context.h>
#include <types.hh>


/** The impalajit namespace is used to encapsulate the three parser classes
 * impalajit::Parser, impalajit::Scanner and impalajit::Driver */
namespace impalajit {

/** The Driver class brings together all components. It creates an instance of
 * the Parser and Scanner classes and connects them. Then the input stream is
 * fed into the scanner object and the parser gets it's token
 * sequence. Furthermore the driver object is available in the grammar rules as
 * a parameter. Therefore the driver class contains a reference to the
 * structure into which the parsed data is saved. */
class Driver
{
private:
    FunctionContext* functionContext;
public:

    ~Driver();

    /** Pointer to the current lexer instance, this is used to connect the
    * parser to the scanner. It is used in the yylex macro. */
    class Scanner* lexer;

    /** Invoke the scanner and parser for a stream.
     * @param in	input stream
     * @param sname	stream name for error messages
     * @return		true if successfully parsed
     */
    std::map<std::string,dasm_gen_func> parse_stream(std::istream& in);

    /** Invoke the scanner and parser on an input string.
     * @param input	input string
     * @param sname	stream name for error messages
     * @return		true if successfully parsed
     */
    std::map<std::string,dasm_gen_func> parse_string(const std::string& input);

    void setFunctionContext(FunctionContext* _functionContext);

    // To demonstrate pure handling of parse errors, instead of
    // simply dumping them on the standard error output, we will pass
    // them to the driver using the following two member functions.

    /** Error handling with associated line number. This can be modified to
     * output the error e.g. to a dialog box. */
    void error(const class location& l, const std::string& m);

    /** General error handling. This can be modified to output the error
     * e.g. to a dialog box. */
    void error(const std::string& m);
};

} // namespace impalajit

#endif // IMPALAJIT_DRIVER_H
